// Helper function to get all cities
export const getAllCities = () => {
  return Object.keys(indianCitiesData);
};

// Helper function to get areas for a specific city
export const getAreasByCity = (city) => {
  if (indianCitiesData[city]) {
    return Object.keys(indianCitiesData[city].areas);
  }
  return [];
};

// Helper function to get pincode for a specific city and area
export const getPincodeByCityAndArea = (city, area) => {
  if (indianCitiesData[city] && indianCitiesData[city].areas[area]) {
    return indianCitiesData[city].areas[area];
  }
  return '';
};

// Helper function to get state for a specific city
export const getStateByCity = (city) => {
  if (indianCitiesData[city]) {
    return indianCitiesData[city].state;
  }
  return '';
};

// Helper function to search cities (for autocomplete)
export const searchCities = (query) => {
  const cities = getAllCities();
  return cities.filter(city => 
    city.toLowerCase().includes(query.toLowerCase())
  );
};

// Helper function to search areas within a city
export const searchAreas = (city, query) => {
  const areas = getAreasByCity(city);
  return areas.filter(area => 
    area.toLowerCase().includes(query.toLowerCase())
  );
};
